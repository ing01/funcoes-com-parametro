# Algoritmo com uma função/método sem parâmetro, para ler um valor e chamar/criar OUTRA função (com parâmetro) que mostre se o valor é par ou ímpar.

def num():
  n = int(input('Digite um valor: '))
  verificar(n)

def verificar(n):
  if (n % 2) == 0:
    print('Par.')
  else:
    print('Ímpar.')

def main():
  num()

main()
